from __future__ import annotations

import argparse
import io
import json
import re
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Any

import numpy as np
import torch
import torch.nn.functional as F
from PIL import Image
from torch import Tensor
from torchmetrics.functional.image import peak_signal_noise_ratio, structural_similarity_index_measure
from tqdm import tqdm

CURRENT_FILE = Path(__file__).resolve()
SANA_ROOT = CURRENT_FILE.parents[1]
PROJECT_ROOT = CURRENT_FILE.parents[4]
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))
if str(SANA_ROOT) not in sys.path:
    sys.path.insert(0, str(SANA_ROOT))


SUPPORTED_METRICS = {"fid", "is", "kid", "psnr", "ssim"}


@dataclass(slots=True)
class SampleRecord:
    dataset_index: int
    dataset_key: str
    sampler: str
    cfg_ratio: float
    generated_path: Path


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Evaluate Sana SAM270k generation metrics (FID/IS/KID/PSNR/SSIM).")
    parser.add_argument(
        "--metadata-jsonl",
        type=Path,
        default=Path("src/stage2/generative/Sana/outputs_sam270k_infer/metadata.jsonl"),
        help="Metadata jsonl generated by infer_SAM270k.py.",
    )
    parser.add_argument(
        "--real-litdata-dir",
        type=str,
        default="data2/RemoteSAM270k/LitData_hyper_images2",
        help="LitData directory containing SAM270k real images.",
    )
    parser.add_argument(
        "--metrics",
        type=str,
        default="fid,is,kid,psnr,ssim",
        help="Comma separated metrics from: fid,is,kid,psnr,ssim",
    )
    parser.add_argument(
        "--sampler",
        type=str,
        default="all",
        help="Filter by sampler (flow_euler/flow_dpm-solver/dpm-solver), or `all`.",
    )
    parser.add_argument(
        "--cfg-ratio",
        type=str,
        default="all",
        help="Filter by cfg ratio (e.g. 4.5), or `all`.",
    )
    parser.add_argument("--max-samples", type=int, default=-1, help="Max number of samples, -1 means all.")
    parser.add_argument("--device", type=str, default="cuda:0", help="Metric computation device.")
    parser.add_argument(
        "--save-json",
        type=Path,
        default=Path("src/stage2/generative/Sana/outputs_sam270k_infer/metrics_report.json"),
        help="Where to save metric report.",
    )
    parser.add_argument(
        "--strict-key-check",
        action="store_true",
        help="Raise error when real sample key mismatches metadata key.",
    )
    return parser.parse_args()


def parse_metrics(metrics_csv: str) -> list[str]:
    metrics = [metric.strip().lower() for metric in metrics_csv.split(",") if metric.strip()]
    if not metrics:
        raise ValueError("`--metrics` is empty.")
    invalid_metrics = [metric for metric in metrics if metric not in SUPPORTED_METRICS]
    if invalid_metrics:
        raise ValueError(f"Unsupported metrics: {invalid_metrics}. Supported: {sorted(SUPPORTED_METRICS)}")
    ordered_unique: list[str] = []
    for metric in metrics:
        if metric not in ordered_unique:
            ordered_unique.append(metric)
    return ordered_unique


def resolve_path(path_str: str) -> str:
    path = Path(path_str)
    if path.is_absolute() or path.exists():
        return str(path)
    sana_path = SANA_ROOT / path
    if sana_path.exists():
        return str(sana_path)
    project_path = PROJECT_ROOT / path
    if project_path.exists():
        return str(project_path)
    return str(path)


def choose_device(device_str: str) -> torch.device:
    requested = torch.device(device_str)
    if requested.type == "cuda" and not torch.cuda.is_available():
        print("[warn] CUDA unavailable, fallback to CPU")
        return torch.device("cpu")
    return requested


def safe_name(name: str) -> str:
    cleaned = re.sub(r"[^0-9A-Za-z._-]+", "_", name).strip("._")
    return cleaned if cleaned else "sample"


def normalize_key(raw_key: Any) -> str:
    if isinstance(raw_key, (list, tuple)):
        value = str(raw_key[0]) if raw_key else ""
    else:
        value = str(raw_key)
    return safe_name(value)


def _parse_cfg_ratio_filter(cfg_ratio: str) -> float | None:
    if cfg_ratio.strip().lower() == "all":
        return None
    return float(cfg_ratio)


def read_records(
    metadata_jsonl: Path,
    sampler: str,
    cfg_ratio: float | None,
    max_samples: int,
) -> list[SampleRecord]:
    if not metadata_jsonl.exists():
        raise FileNotFoundError(f"metadata jsonl not found: {metadata_jsonl}")

    records: list[SampleRecord] = []
    with metadata_jsonl.open("r", encoding="utf-8") as fp:
        for line in fp:
            if not line.strip():
                continue
            raw = json.loads(line)
            record = SampleRecord(
                dataset_index=int(raw["dataset_index"]),
                dataset_key=safe_name(str(raw.get("dataset_key", ""))),
                sampler=str(raw.get("sampler", "")),
                cfg_ratio=float(raw.get("cfg_ratio", 0.0)),
                generated_path=Path(str(raw["output"])),
            )
            if sampler != "all" and record.sampler != sampler:
                continue
            if cfg_ratio is not None and abs(record.cfg_ratio - cfg_ratio) > 1e-8:
                continue
            if not record.generated_path.exists():
                alt_path = PROJECT_ROOT / record.generated_path
                if alt_path.exists():
                    record.generated_path = alt_path
            if record.generated_path.exists():
                records.append(record)
            if max_samples > 0 and len(records) >= max_samples:
                break

    if not records:
        raise RuntimeError("No valid samples found after filtering.")
    return records


def load_generated_image(path: Path) -> Tensor:
    with Image.open(path) as image:
        rgb = np.array(image.convert("RGB"), dtype=np.uint8, copy=True)
        tensor = torch.from_numpy(rgb).permute(2, 0, 1).contiguous()
    return tensor


def load_litdata_dataset(real_litdata_dir: str):
    import litdata

    import src.data.codecs  # noqa: F401

    return litdata.StreamingDataset(input_dir=real_litdata_dir)


def _to_rgb_chw_uint8(value: Any) -> Tensor:
    if torch.is_tensor(value):
        tensor = value.detach().cpu()
    elif isinstance(value, np.ndarray):
        tensor = torch.from_numpy(value)
    elif isinstance(value, Image.Image):
        arr = np.array(value.convert("RGB"), dtype=np.uint8, copy=True)
        tensor = torch.from_numpy(arr)
    elif isinstance(value, bytes):
        with Image.open(io.BytesIO(value)) as image:
            arr = np.array(image.convert("RGB"), dtype=np.uint8, copy=True)
        tensor = torch.from_numpy(arr)
    else:
        raise TypeError(f"Unsupported image type: {type(value)}")

    if tensor.ndim == 2:
        tensor = tensor.unsqueeze(-1)
    if tensor.ndim != 3:
        raise ValueError(f"Expected 2D/3D image-like tensor, got shape={tuple(tensor.shape)}")

    if tensor.shape[0] in (1, 3, 4) and tensor.shape[-1] not in (1, 3, 4):
        chw = tensor
    else:
        chw = tensor.permute(2, 0, 1)

    if chw.shape[0] == 1:
        chw = chw.repeat(3, 1, 1)
    elif chw.shape[0] > 3:
        chw = chw[:3]

    chw = chw.to(torch.float32)
    min_v = float(chw.min().item())
    max_v = float(chw.max().item())
    if max_v <= 1.0 and min_v >= 0.0:
        chw = chw * 255.0
    elif min_v >= -1.0 and max_v <= 1.0:
        chw = (chw + 1.0) * 127.5
    chw = torch.clamp(chw, 0.0, 255.0).to(torch.uint8)
    return chw


def load_real_image(ds: Any, dataset_index: int, expected_key: str, strict_key_check: bool) -> Tensor:
    sample = ds[dataset_index]
    if sample is None:
        raise RuntimeError(f"Cannot decode real sample at index {dataset_index}")

    real_key = normalize_key(sample.get("__key__", dataset_index))
    if real_key != expected_key:
        message = f"Key mismatch at idx={dataset_index}: got `{real_key}`, expected `{expected_key}`"
        if strict_key_check:
            raise RuntimeError(message)
        print(f"[warn] {message}")

    if "img" not in sample:
        raise KeyError("Field `img` not found in litdata sample.")
    return _to_rgb_chw_uint8(sample["img"])


def resize_fake_to_real(fake: Tensor, real: Tensor) -> Tensor:
    if fake.shape[-2:] == real.shape[-2:]:
        return fake
    fake_bchw = fake.unsqueeze(0)
    resized = F.interpolate(fake_bchw, size=real.shape[-2:], mode="bicubic", align_corners=False, antialias=True)
    return resized.squeeze(0)


def build_metrics(metrics: list[str], device: torch.device) -> dict[str, Any]:
    metric_objects: dict[str, Any] = {}
    if "fid" in metrics:
        from torchmetrics.image.fid import FrechetInceptionDistance

        metric_objects["fid"] = FrechetInceptionDistance(feature=2048, normalize=False).to(device)
    if "is" in metrics:
        from torchmetrics.image.inception import InceptionScore

        metric_objects["is"] = InceptionScore(normalize=False).to(device)
    if "kid" in metrics:
        from torchmetrics.image.kid import KernelInceptionDistance

        metric_objects["kid"] = KernelInceptionDistance(feature=2048, normalize=False).to(device)
    return metric_objects


def evaluate(
    records: list[SampleRecord],
    ds: Any,
    metrics: list[str],
    device: torch.device,
    strict_key_check: bool,
) -> dict[str, float | int]:
    metric_objects = build_metrics(metrics, device)
    paired_count = 0
    ssim_sum = 0.0
    psnr_sum = 0.0

    with torch.no_grad():
        for record in tqdm(records, total=len(records), desc="Evaluating", unit="img"):
            fake_uint8 = load_generated_image(record.generated_path)
            real_uint8 = load_real_image(
                ds=ds,
                dataset_index=record.dataset_index,
                expected_key=record.dataset_key,
                strict_key_check=strict_key_check,
            )

            fake_batch = fake_uint8.unsqueeze(0).to(device)
            real_batch = real_uint8.unsqueeze(0).to(device)

            if "fid" in metric_objects:
                metric_objects["fid"].update(real_batch, real=True)
                metric_objects["fid"].update(fake_batch, real=False)
            if "kid" in metric_objects:
                metric_objects["kid"].update(real_batch, real=True)
                metric_objects["kid"].update(fake_batch, real=False)
            if "is" in metric_objects:
                metric_objects["is"].update(fake_batch)

            if "psnr" in metrics or "ssim" in metrics:
                fake_float = fake_uint8.float() / 255.0
                real_float = real_uint8.float() / 255.0
                fake_float = resize_fake_to_real(fake=fake_float, real=real_float)
                fake_b = fake_float.unsqueeze(0)
                real_b = real_float.unsqueeze(0)
                if "psnr" in metrics:
                    psnr_sum += float(peak_signal_noise_ratio(fake_b, real_b, data_range=1.0).item())
                if "ssim" in metrics:
                    ssim_sum += float(structural_similarity_index_measure(fake_b, real_b, data_range=1.0).item())
                paired_count += 1

    results: dict[str, float | int] = {}
    if "fid" in metric_objects:
        results["fid"] = float(metric_objects["fid"].compute().detach().cpu().item())
    if "is" in metric_objects:
        is_mean, is_std = metric_objects["is"].compute()
        results["is_mean"] = float(is_mean.detach().cpu().item())
        results["is_std"] = float(is_std.detach().cpu().item())
    if "kid" in metric_objects:
        kid_mean, kid_std = metric_objects["kid"].compute()
        results["kid_mean"] = float(kid_mean.detach().cpu().item())
        results["kid_std"] = float(kid_std.detach().cpu().item())
    if "psnr" in metrics:
        results["psnr"] = float(psnr_sum / max(paired_count, 1))
    if "ssim" in metrics:
        results["ssim"] = float(ssim_sum / max(paired_count, 1))
    results["num_samples"] = len(records)
    return results


def main() -> None:
    args = parse_args()
    metrics = parse_metrics(args.metrics)
    cfg_ratio_filter = _parse_cfg_ratio_filter(args.cfg_ratio)
    device = choose_device(args.device)
    records = read_records(
        metadata_jsonl=args.metadata_jsonl,
        sampler=args.sampler,
        cfg_ratio=cfg_ratio_filter,
        max_samples=args.max_samples,
    )

    litdata_dir = resolve_path(args.real_litdata_dir)
    ds = load_litdata_dataset(litdata_dir)

    results = evaluate(
        records=records,
        ds=ds,
        metrics=metrics,
        device=device,
        strict_key_check=args.strict_key_check,
    )

    report = {
        "metadata_jsonl": str(args.metadata_jsonl),
        "real_litdata_dir": str(litdata_dir),
        "sampler": args.sampler,
        "cfg_ratio": args.cfg_ratio,
        "metrics": metrics,
        "device": str(device),
        "results": results,
    }
    args.save_json.parent.mkdir(parents=True, exist_ok=True)
    args.save_json.write_text(json.dumps(report, indent=2, ensure_ascii=False), encoding="utf-8")
    print(json.dumps(report, indent=2, ensure_ascii=False))
    print(f"Saved report to: {args.save_json}")


