RGB_REFERENCE_METRICS = {"fid", "is", "kid", "psnr", "ssim"}
RGB_NO_REFERENCE_METRICS = {"niqe", "clipiqa", "brisque"}
RGB_METRICS = RGB_REFERENCE_METRICS | RGB_NO_REFERENCE_METRICS
SPECTRAL_METRICS = {"spr", "srec"}
SUPPORTED_METRICS = RGB_METRICS | SPECTRAL_METRICS


@dataclass(slots=True)
class SampleRecord:
    dataset_index: int
    dataset_key: str
    sampler: str
    cfg_ratio: float
    generated_path: Path
    generated_hsi_tif_path: Path


